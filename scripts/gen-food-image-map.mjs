#!/usr/bin/env node

/**
 * Generates lib/food-images.ts from files in public/food/
 *
 * Each filename becomes a keyword match:
 *   asha-idli.png → "asha-idli" matches items containing those words
 *   pizza.png → "pizza" matches items containing "pizza"
 *
 * Restaurant-specific images (e.g. asha-masala-dosa.png) are detected
 * by known restaurant prefixes and matched only for that restaurant.
 *
 * Run: node scripts/gen-food-image-map.mjs
 */

import fs from "fs"
import path from "path"

const FOOD_DIR = "./public/food"
const OUTPUT = "./lib/food-images.ts"

const RESTAURANT_PREFIXES = {
  "asha": "Asha Tiffins",
  "idc": "IDC Kitchen",
}

const files = fs.readdirSync(FOOD_DIR).filter(f => /\.(png|jpg|jpeg|webp)$/i.test(f))

const restaurantMap = {} // { "Asha Tiffins": { "idli": "/food/asha-idli.png" } }
const genericMap = {} // { "pizza": "/food/pizza.png" }

for (const file of files) {
  const slug = file.replace(/\.[^.]+$/, "") // remove extension
  const imagePath = `/food/${file}`

  // Check if it starts with a restaurant prefix
  let isRestaurantSpecific = false
  for (const [prefix, restaurant] of Object.entries(RESTAURANT_PREFIXES)) {
    if (slug.startsWith(prefix + "-")) {
      const keyword = slug.slice(prefix.length + 1).replace(/-/g, " ")
      if (!restaurantMap[restaurant]) restaurantMap[restaurant] = {}
      restaurantMap[restaurant][keyword] = imagePath
      isRestaurantSpecific = true
      break
    }
  }

  if (!isRestaurantSpecific) {
    const keyword = slug.replace(/-/g, " ")
    genericMap[keyword] = imagePath
  } else {
    // Also add as generic fallback (first one wins)
    const keyword = slug.slice(slug.indexOf("-") + 1).replace(/-/g, " ")
    if (!genericMap[keyword]) genericMap[keyword] = imagePath
  }
}

// Generate TypeScript
let output = `// Auto-generated by scripts/gen-food-image-map.mjs
// Run: node scripts/gen-food-image-map.mjs

export const RESTAURANT_IMAGE_MAP: Record<string, Record<string, string>> = ${JSON.stringify(restaurantMap, null, 2)}

export const FOOD_IMAGE_MAP: Record<string, string> = ${JSON.stringify(genericMap, null, 2)}

function matchesKeyword(name: string, keyword: string): boolean {
  // Match if any word from the keyword is found in the item name
  return keyword.split(" ").some((word) => name.includes(word))
}

export function getItemImage(itemName: string, restaurant: string): string | null {
  const name = itemName.toLowerCase()
  const restMap = RESTAURANT_IMAGE_MAP[restaurant]
  if (restMap) {
    for (const [keyword, image] of Object.entries(restMap)) {
      if (matchesKeyword(name, keyword)) return image
    }
  }
  for (const [keyword, image] of Object.entries(FOOD_IMAGE_MAP)) {
    if (matchesKeyword(name, keyword)) return image
  }
  return null
}

export function getFoodImage(items: { name: string }[], restaurant: string): string | null {
  for (const item of items) {
    const img = getItemImage(item.name, restaurant)
    if (img) return img
  }
  return null
}
`

fs.writeFileSync(OUTPUT, output)
console.log(`Generated ${OUTPUT} from ${files.length} images`)
console.log("Restaurant-specific:", Object.entries(restaurantMap).map(([r, m]) => `${r}: ${Object.keys(m).join(", ")}`).join("; "))
console.log("Generic:", Object.keys(genericMap).join(", "))
